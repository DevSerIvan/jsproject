// Конструкция switch, поддерживает несколько условий, аналог if else
// В отличие от обычных условий, switch всегда идет на строгое соответствие
// Проверку можно делать с числами, но и со строками

const nuM = 10;
switch (nuM) {
    case 40: // здесь идет сравнение условия на истинность с nuM 
        console.log('Неверно'); // и выводится ответ
        break;  // это обязательный синтаксис  в конце после case
    case 100:
        console.log('Неверно');
        break;
    case 50:
        console.log('Правда!');
        break;
    default:       // если ни один case не подходит, то по-умолчанию
        console.log('Не совсем так!');
        break;
}