// Условия

// Простое условие
if (5 == 10) {  // после условия сразу проверяется истинность выражения
    console.log('YES'); // если условия выше истинно, то выводится 'YES'
}

// Вариант 2 простого условия.
if (7) {  // сразу указываем, что условие истинное (может быть что угодно)
    console.log('YES'); // конечно выводится 'YES' (т.к. условие истинно!)
}

// Условие с else
